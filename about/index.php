                    <h2>About CSSA</h2>
                    <hr>
                    <p>CSSA，全称Chinese Students and Scholars Association, 是在如今世界各地的高校中都不乏其身影的学生组织。中国学生出国留学史可以追溯到几百年前，但是一个专门为中国留学生服务的组织的成立时间却只有短短三十来年。世界上第一个CSSA到底是在什么时候成立的，又是在哪所学校成立的，我们不得而知。但是据有关记录显示，自二十世纪八十年代起，CSSA的身影就如雨后春笋般的出现在了全世界的各个高校。
<p>美国是中国留学出口的第一目的地。自然地，CSSA的脚步也就遍布这片宽阔的新大陆。无论是著名的常青藤私立大学，还是实力雄厚的州立研究型大学，亦或是小却精的文理学院，都陆陆续续地成立起了中国学生学者联合会，为的就是给一年比一年数量大幅增长的中国留学生和学者提供一个可以依靠的、温暖人心的地方。</p>
<p>香港、加拿大和澳大利亚的中国人留学历史也非常悠久。在香港，CSSA常常被称做内地学生学者联谊会。而加拿大在各个方面都跟邻国美国比较相似，连CSSA出现的时间也相差无几，皆为上世纪八十年代初。作为一个老牌中国留学生和学者目的地的澳大利亚，自然也有丰富的CSSA历史和文化。与美国一样，澳大利亚的各个高校也各自成立了CSSA。每校各有特色，也时不时会联合起来举行活动。</p>
<p>欧洲的CSSA出现的时间相较于之前提到的几个地区就稍稍晚了一些，却别有一番特色。或许是因为欧洲国家面积狭小，人口不多的原因，很多欧洲国家都有一个以国家为单位的总CSSA。这种“国家级别”的CSSA旗下才是各个大学或城市的CSSA分会。在这些以国家为单位的CSSA中，全英学联可以说是一个出色的代表。全英学联是为全英国各地中国学生学者服务的团体，自1988年成立至今已历经二十二届，拥有近一百个地方学联会员单位。近些年考虑到经济，地理因素等，以新加坡为首的东南亚国家也越来越受留学学生学者的欢迎。在这些东南亚国家留学的中国人们和去了欧洲各国同胞们一样，也成立了联合全国各个CSSA的总学联，把分散在不同学校的同伴们紧紧地联系在了一起。
<p>随着中国越来越国际化，人们的思想也越来越开放大胆，一些不曾是留学焦点的地方也渐渐有了愿意深入了解这个地方的人。连中国大众不太了解的非洲大陆也出现了CSSA的身影。埃及中国学联是由中国旅埃各民族留学生和学者志愿组成的公益性组织，正式成立于 2011 年 5 月 21 日 ，接受中华人民共和国驻阿拉伯埃及共和国大使馆教育处的领导，是目前埃及华人中规模最大的社团组织之一。</p>
<p>CSSA自成立以来几十年一直为世界各地在异国他乡奋斗的中国人们服务，致力于为他们创造出一个家外之家的氛围，举办各种或温暖人心或实用可靠的活动。CSSA早已不仅仅是一个高校里的学生组织了。每一个学校的CSSA都为了给大家提供更好的服务，更强力的帮助积极地与当地组织、政府联系，已经成了海外华人群体中不可忽视的重要领导者。各个地区的CSSA逐渐都与当地中国大使馆建立了合作关系，为促进当地与中国的友好交流做出了很大的贡献。CSSA组织的中国特色活动，例如每年的春节晚会，都会引来各地媒体的高度关注。美国各个大型学校中，若是华人密集成员众多的CSSA会各自举办精彩的春节晚会，而成员相对较少的CSSA便与附近的其他分会一起联合举办。中国国内的领导们也对身在海外的学生学者们非常重视。历届（什么主席？国家？）主席中都不乏有在探访他国时亲切接见当地CSSA成员的事迹，为当地的学生学生们送上祝福与温暖。</p>
<p>全世界有数百个CSSA，每一个都仅仅诞生于三五个人的一腔热情，却在经历了时间的磨练后壮大成了浩浩荡荡的大家庭。在与社会各方打交道的过程中，CSSA越来越成熟，存在感也日渐增强。现在每当提到海外留学、工作，首先进入人们脑海的就是CSSA这个永远欢迎着中国同胞的热情团体。CSSA为初到他国的学生学者们铺好了前进的道路，送上了欢迎和鼓励；为在异国他乡奋斗了几年的人们提供了无条件的支持和无尽头的欢乐；为站在回国或继续留在他国发展路口的中国同胞们准备了丰富的资料和专业帮助。我们相信，无论是哪里的CSSA，无论是哪种形式的CSSA，在未来都会有更进一步的发展，变得更加贴心，更加热情，更加强大。</p>

